pub fn center_shape(points: &mut Vec<PipelinePoint>) {
  let mut sum_x = 0.0;
  let mut sum_y = 0.0;

  for mut pt in points.iter_mut() {
    sum_x += pt.x;
    sum_y += pt.y;
  }

  let avg_x = sum_x / points.len() as f32;
  let avg_y = sum_y / points.len() as f32;

  println!("Avg: {}, {}", avg_x, avg_y);

  for mut pt in points.iter_mut() {
    pt.x -= avg_x;
    pt.y -= avg_y;

    pt.x *= -50.0;
    pt.y *= -50.0;
  }
}

